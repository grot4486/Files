a = int(input('Необходимо ввести коэффициент a: '))
b = int(input('Необходимо ввести коэффициент b: '))
c = int(input('Необходимо ввести коэффициент c: '))

D = b ** 2 - 4 * a * c

if D == 0:
    x = (-b / (2 * a))
    print(f'Корень уравнения равен - {x}')
elif D > 0:
    y = ((-b + (D ** 0.5)) / (2 * a))
    z = ((-b - (D ** 0.5)) / (2 * a))
    print(f'Корни уравнения равны- ({y}, {z})')
else:
    print('Корней нет')
